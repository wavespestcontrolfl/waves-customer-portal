/**
 * backlink-profile-astro-sync.js — Pillar 1 "close the loop" automation.
 *
 * Pushes the Backlink Manager's VERIFIED-LIVE off-site profile listings into the
 * marketing site's schema.org sameAs, by opening a PR that overwrites
 * wavespestcontrol-astro:src/data/entity-profiles.auto.json. The Astro side
 * (entity-profiles.ts) merges that file into the corporate Organization sameAs
 * and re-validates every URL, so a bad value can never reach schema.
 *
 * SOURCE: seo_link_prospects — the active Hermes/link-prospect lane. We take only
 * rows the portal's verifier independently confirmed live (status='live') whose
 * link_type is an entity-PROFILE kind (directory/citation/social), i.e. listings
 * that REPRESENT the business. Editorial/guest-post/HARO/resource links are
 * inbound backlinks from other sites — NOT our profiles — so they are excluded
 * from sameAs.
 *
 * Scope: CORPORATE only. Auto-acquired listings use the one Waves business
 * identity, so they're corporate-level; per-office profiles (Apple/Bing per
 * location) stay hand-curated in entity-profiles.ts.
 *
 * Safety:
 *  - Opens a PR for human review; never auto-merges.
 *  - No-ops (no PR) when the verified set is unchanged.
 *  - Gate off OR dryRun → never writes; returns what it WOULD do.
 *  - Bails if the target file is missing (astro PR not merged yet) rather than
 *    forking the source of truth.
 */

const logger = require('./logger');
const db = require('../models/db');
const gh = require('./content-astro/github-client');
const { isEnabled } = require('../config/feature-gates');

const ASTRO_FILE = 'src/data/entity-profiles.auto.json';
const SYNC_BRANCH_PREFIX = 'data/entity-profiles-sync-';

const FILE_NOTE =
  'AUTO-GENERATED by the portal Backlink Manager (waves-customer-portal: ' +
  'server/services/backlink-profile-astro-sync.js). DO NOT hand-edit — it is ' +
  'overwritten on each sync via PR. Hand-curated profiles go in ' +
  'entity-profiles.ts. Holds VERIFIED + LIVE corporate profile URLs only; ' +
  'merged into the Organization sameAs by entity-profiles.ts (cleanProfileUrls ' +
  're-validates everything here).';

// link_type values that are PROFILES representing the business (sameAs-eligible).
// Excludes editorial/guest_post/haro/resource (inbound links from other sites).
const PROFILE_LINK_TYPES = ['directory', 'citation', 'social'];

// Statuses for a confirmed-live placement. The verifier sets 'live'; the indexer
// then advances 'live' → 'indexed' once Google indexes it (and reverts to 'live'
// if it drops out — link-prospect-indexer.js). BOTH mean the profile is live, so
// include both, else an indexed profile silently drops out of sameAs on the next
// overwrite.
const VERIFIED_LIVE_STATUSES = ['live', 'indexed'];

/**
 * Verified-live corporate profile URLs (https-only, de-duped, sorted). status is
 * 'live'/'indexed', set by the portal's own verifier + indexer (never trusted
 * from a worker self-report), so only independently-confirmed listings ship.
 */
async function getVerifiedCorporateProfileUrls() {
  // CORPORATE scope only — exclude per-LOCATION listings. The signup runner picks
  // a GBP location per prospect; per-office listings stamp quality_signals.location
  // (one listing per office per directory), while a default/brand-level listing
  // leaves it unset. Per-office profiles are hand-curated in entity-profiles.ts, so
  // only unscoped (location-null) rows feed the corporate Organization sameAs.
  const rows = await db('seo_link_prospects')
    .whereIn('status', VERIFIED_LIVE_STATUSES)
    .whereIn('link_type', PROFILE_LINK_TYPES)
    .whereNotNull('live_url')
    .whereRaw("(quality_signals->>'location') IS NULL")
    .select('live_url');

  const seen = new Set();
  for (const r of rows) {
    const u = String(r.live_url || '').trim();
    if (/^https:\/\/\S+$/.test(u)) seen.add(u);
  }
  return [...seen].sort();
}

function buildPayload(urls) {
  return (
    JSON.stringify({ _note: FILE_NOTE, generatedAt: new Date().toISOString(), corporate: urls }, null, 2) + '\n'
  );
}

function parseCurrentCorporate(existing) {
  if (!existing || !existing.content) return [];
  try {
    const arr = JSON.parse(existing.content).corporate;
    return Array.isArray(arr) ? arr.map((u) => String(u)) : [];
  } catch {
    return [];
  }
}

/**
 * Sync verified profiles → astro entity-profiles.auto.json via a reviewable PR.
 * @param {{dryRun?: boolean}} opts
 * @returns {Promise<object>} result summary
 */
async function syncProfilesToAstro({ dryRun = false } = {}) {
  const gateOn = isEnabled('backlinkProfileSync');
  const urls = await getVerifiedCorporateProfileUrls();

  // getFile returns null on a genuine 404 (file absent) and THROWS on auth/network
  // errors (missing GITHUB_TOKEN, 403, transient 5xx). On a live run those MUST
  // surface (so the cron/CLI fails loudly) rather than be misread as target_missing
  // and silently no-op; for a dry-run / gate-off preview we tolerate them.
  let existing;
  try {
    existing = await gh.getFile(ASTRO_FILE);
  } catch (e) {
    if (gateOn && !dryRun) throw e;
    logger.warn(`[backlink-sync] getFile(${ASTRO_FILE}) failed (${e.message}); continuing for ${dryRun ? 'dry-run' : 'gate-off'} preview`);
    existing = null;
  }
  const current = parseCurrentCorporate(existing);
  const changed = current.slice().sort().join('\n') !== urls.join('\n');

  // Gate off or explicit dry-run → never writes. Report what would happen.
  if (!gateOn || dryRun) {
    logger.info(
      `[backlink-sync] ${dryRun ? 'dry-run' : 'gate-off'}: ${changed ? 'WOULD update' : 'no change'} ` +
      `(${urls.length} verified profiles, was ${current.length})`,
    );
    return { wrote: false, reason: dryRun ? 'dry_run' : 'gate_off', changed, count: urls.length, urls };
  }

  if (!changed) {
    logger.info(`[backlink-sync] no change (${urls.length} verified profiles) — no PR`);
    return { wrote: false, reason: 'unchanged', changed: false, count: urls.length };
  }

  // The target file is created by the astro PR; don't fork the source of truth.
  if (!existing) {
    logger.error(`[backlink-sync] ${ASTRO_FILE} missing on default branch (astro PR not merged) — skipping`);
    return { wrote: false, reason: 'target_missing', changed: true, count: urls.length };
  }

  const content = buildPayload(urls);
  const message = `chore(data): sync ${urls.length} verified entity profiles`;
  const prTitle = `Sync ${urls.length} verified entity profiles (Backlink Manager)`;
  const prBody =
    `Auto-generated by the portal Backlink Manager — verified, live off-site ` +
    `profiles to add to the Organization \`sameAs\`.\n\n` +
    `**${urls.length} profile(s)** (was ${current.length}):\n` +
    urls.map((u) => `- ${u}`).join('\n') +
    `\n\nReview the URLs (each should be a real, claimed, NAP-matched Waves ` +
    `profile), then merge. Hand-curated profiles live in \`entity-profiles.ts\`.`;
  const { owner, repo } = gh.env();

  // Reuse an OPEN sync PR if one is already awaiting review — human review can
  // take longer than the weekly interval, and the default branch keeps the old
  // list until merge, so a fresh branch each run would stack duplicate PRs with
  // the same URLs. Update the open PR's branch in place instead.
  const openPrs = await gh
    .ghFetchPaginated(`/repos/${owner}/${repo}/pulls?state=open`)
    .catch(() => []);
  const openSyncPr = (openPrs || []).find(
    (p) => p && p.head && typeof p.head.ref === 'string' && p.head.ref.startsWith(SYNC_BRANCH_PREFIX),
  );

  if (openSyncPr) {
    const branch = openSyncPr.head.ref;
    const onBranch = await gh.getFile(ASTRO_FILE, branch).catch(() => null);
    await gh.putFile({ path: ASTRO_FILE, content, message, branch, sha: onBranch ? onBranch.sha : existing.sha });
    // Refresh title/body too — the diff now reflects a different set, so stale
    // count/URL review instructions would otherwise mislead the reviewer.
    await gh.updatePr(openSyncPr.number, { title: prTitle, body: prBody });
    logger.info(`[backlink-sync] updated open PR ${openSyncPr.html_url} (${urls.length} profiles)`);
    return { wrote: true, updatedExisting: true, count: urls.length, pr: openSyncPr.html_url, branch };
  }

  const stamp = new Date().toISOString().replace(/[^0-9]/g, '').slice(0, 14);
  const branch = `${SYNC_BRANCH_PREFIX}${stamp}`;
  await gh.createBranch(branch);
  await gh.putFile({ path: ASTRO_FILE, content, message, branch, sha: existing.sha });
  const pr = await gh.createPr({ head: branch, title: prTitle, body: prBody });
  logger.info(`[backlink-sync] opened PR ${pr.html_url} (${urls.length} profiles)`);
  return { wrote: true, count: urls.length, pr: pr.html_url, branch };
}

module.exports = { syncProfilesToAstro, getVerifiedCorporateProfileUrls };
