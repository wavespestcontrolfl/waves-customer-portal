/**
 * backlink-profile-astro-sync.js — Pillar 1 "close the loop" automation.
 *
 * Pushes the Backlink Manager's VERIFIED-LIVE off-site profile listings into the
 * marketing site's schema.org sameAs, by opening a PR that overwrites
 * wavespestcontrol-astro:src/data/entity-profiles.auto.json. The Astro side
 * (entity-profiles.ts) merges each office bucket into that office's LocalBusiness
 * sameAs and re-validates every URL, so a bad value can never reach schema.
 *
 * SOURCE: seo_link_prospects — the active Hermes/link-prospect lane. We take only
 * rows the portal's own verifier/indexer confirmed live (status live/indexed,
 * never a worker self-report) whose link_type is an entity-PROFILE kind
 * (directory/citation/social), i.e. listings that REPRESENT the business.
 * Editorial/guest_post/HARO/resource links are inbound backlinks from other
 * sites — NOT our profiles — so they are excluded from sameAs.
 *
 * SCOPE: PER-OFFICE. The signup runner maps every citation to a GBP location
 * (quality_signals.location); a default/brand-level listing maps to the default
 * location (Bradenton). So listings are grouped by office slug, not "corporate".
 * Brand-level profiles (Apple/Bing/Crunchbase) are hand-curated in
 * entity-profiles.ts.
 *
 * Safety:
 *  - Opens a PR for human review; never auto-merges.
 *  - No-ops (no PR) when the verified set is unchanged.
 *  - Gate off OR dryRun → never writes; returns what it WOULD do.
 *  - Bails if the target file is missing (astro PR not merged yet) rather than
 *    forking the source of truth; verifies repo access so a misconfig isn't
 *    silently read as "file absent".
 */

const logger = require('./logger');
const db = require('../models/db');
const gh = require('./content-astro/github-client');
const { isEnabled } = require('../config/feature-gates');

const ASTRO_FILE = 'src/data/entity-profiles.auto.json';
const SYNC_BRANCH_PREFIX = 'data/entity-profiles-sync-';

const FILE_NOTE =
  'AUTO-GENERATED by the portal Backlink Manager (waves-customer-portal: ' +
  'server/services/backlink-profile-astro-sync.js). DO NOT hand-edit — it is ' +
  'overwritten on each sync via PR. Hand-curated profiles go in ' +
  'entity-profiles.ts. Holds VERIFIED + LIVE profile listings grouped by OFFICE ' +
  'slug (every auto-acquired citation maps to a GBP location; default → ' +
  "bradenton-fl); merged into each office's LocalBusiness sameAs by " +
  'entity-profiles.ts (cleanProfileUrls re-validates everything here).';

// link_type values that are PROFILES representing the business (sameAs-eligible).
// Excludes editorial/guest_post/haro/resource (inbound links from other sites).
const PROFILE_LINK_TYPES = ['directory', 'citation', 'social'];

// Statuses for a confirmed-live placement. The verifier sets 'live'; the indexer
// then advances 'live' → 'indexed' once Google indexes it (and reverts to 'live'
// if it drops out — link-prospect-indexer.js). BOTH mean live, so include both.
const VERIFIED_LIVE_STATUSES = ['live', 'indexed'];

// Office slugs that have their OWN LocalBusiness node on the marketing site
// (the GBP offices). Must match entity-profiles.auto.json `offices` keys +
// src/data/waves-offices.ts WAVES_OFFICES. The HQ (Lakewood Ranch) has no node.
const OFFICE_SLUGS = ['bradenton-fl', 'sarasota-fl', 'venice-fl', 'parrish-fl'];

// signup-runner location id → office slug. The runner's CITY_TO_LOCATION collapses
// every SWFL city (incl. service-area-only ones like North Port) to one of these
// four location ids; the default_location_id is 'bradenton'. (Join key behind
// these is googlePlaceId — see waves-offices.ts / config/locations.js.)
const LOCATION_TO_OFFICE_SLUG = {
  bradenton: 'bradenton-fl',
  parrish: 'parrish-fl',
  sarasota: 'sarasota-fl',
  venice: 'venice-fl',
};

function officeSlugForLocation(location) {
  // Brand-level / unscoped listings carry the default location (Bradenton).
  if (!location || location === 'default') return 'bradenton-fl';
  return LOCATION_TO_OFFICE_SLUG[location] || null; // unknown id → drop
}

/**
 * Verified-live profile URLs grouped by office slug. status is live/indexed, set
 * by the portal's own verifier + indexer (never a worker self-report), so only
 * independently-confirmed listings ship. Returns { 'bradenton-fl': [urls], ... }
 * with all OFFICE_SLUGS present (empty arrays) for stable diffs.
 */
async function getVerifiedProfilesByOffice() {
  const rows = await db('seo_link_prospects')
    .whereIn('status', VERIFIED_LIVE_STATUSES)
    .whereIn('link_type', PROFILE_LINK_TYPES)
    .whereNotNull('live_url')
    .select('live_url', db.raw("quality_signals->>'location' as location"));

  const byOffice = {};
  for (const slug of OFFICE_SLUGS) byOffice[slug] = new Set();
  let dropped = 0;
  for (const r of rows) {
    const slug = officeSlugForLocation(r.location);
    const url = String(r.live_url || '').trim();
    if (!slug) { dropped++; continue; }
    if (/^https:\/\/\S+$/.test(url)) byOffice[slug].add(url);
  }
  if (dropped) logger.warn(`[backlink-sync] dropped ${dropped} listing(s) with an unknown location id`);

  const offices = {};
  for (const slug of OFFICE_SLUGS) offices[slug] = [...byOffice[slug]].sort();
  return offices;
}

function totalCount(offices) {
  return Object.values(offices).reduce((n, a) => n + (Array.isArray(a) ? a.length : 0), 0);
}

// Stable JSON of the office map (sorted slugs + sorted urls) for change detection.
function canonicalOffices(offices) {
  const out = {};
  for (const slug of OFFICE_SLUGS) out[slug] = [...(offices[slug] || [])].sort();
  return JSON.stringify(out);
}

function buildPayload(offices) {
  return JSON.stringify({ _note: FILE_NOTE, generatedAt: new Date().toISOString(), offices }, null, 2) + '\n';
}

function parseCurrentOffices(existing) {
  const empty = {};
  for (const slug of OFFICE_SLUGS) empty[slug] = [];
  if (!existing || !existing.content) return empty;
  try {
    const o = JSON.parse(existing.content).offices;
    if (!o || typeof o !== 'object') return empty;
    const out = {};
    for (const slug of OFFICE_SLUGS) out[slug] = Array.isArray(o[slug]) ? o[slug].map(String) : [];
    return out;
  } catch {
    return empty;
  }
}

function buildPrBody(offices, total, currentTotal) {
  const lines = OFFICE_SLUGS
    .filter((slug) => offices[slug] && offices[slug].length)
    .map((slug) => `**${slug}** (${offices[slug].length}):\n` + offices[slug].map((u) => `- ${u}`).join('\n'))
    .join('\n\n');
  return (
    `Auto-generated by the portal Backlink Manager — verified, live off-site ` +
    `listings to add to each office's LocalBusiness \`sameAs\`.\n\n` +
    `**${total} profile(s)** across offices (was ${currentTotal}):\n\n` +
    (lines || '_none_') +
    `\n\nReview the URLs (each should be a real, claimed, NAP-matched Waves ` +
    `listing for that office), then merge. Hand-curated profiles live in ` +
    `\`entity-profiles.ts\`.`
  );
}

/**
 * Sync verified profiles → astro entity-profiles.auto.json via a reviewable PR.
 * @param {{dryRun?: boolean}} opts
 * @returns {Promise<object>} result summary
 */
async function syncProfilesToAstro({ dryRun = false } = {}) {
  const gateOn = isEnabled('backlinkProfileSync');
  const offices = await getVerifiedProfilesByOffice();
  const total = totalCount(offices);

  // getFile returns null on a genuine 404 (file absent) and THROWS on auth/network
  // errors (missing GITHUB_TOKEN, 403, transient 5xx). On a live run those MUST
  // surface (so the cron/CLI fails loudly) rather than be misread as target_missing
  // and silently no-op; for a dry-run / gate-off preview we tolerate them.
  let existing;
  try {
    existing = await gh.getFile(ASTRO_FILE);
  } catch (e) {
    if (gateOn && !dryRun) throw e;
    logger.warn(`[backlink-sync] getFile(${ASTRO_FILE}) failed (${e.message}); continuing for ${dryRun ? 'dry-run' : 'gate-off'} preview`);
    existing = null;
  }
  const current = parseCurrentOffices(existing);
  const currentTotal = totalCount(current);
  const changed = canonicalOffices(current) !== canonicalOffices(offices);

  // Gate off or explicit dry-run → never writes. Report what would happen.
  if (!gateOn || dryRun) {
    logger.info(
      `[backlink-sync] ${dryRun ? 'dry-run' : 'gate-off'}: ${changed ? 'WOULD update' : 'no change'} ` +
      `(${total} verified profiles across ${OFFICE_SLUGS.length} offices, was ${currentTotal})`,
    );
    return { wrote: false, reason: dryRun ? 'dry_run' : 'gate_off', changed, count: total, offices };
  }

  if (!changed) {
    logger.info(`[backlink-sync] no change (${total} verified profiles) — no PR`);
    return { wrote: false, reason: 'unchanged', changed: false, count: total };
  }

  // The target file is created by the astro PR; don't fork the source of truth.
  // null = a GitHub 404 — but a wrong GITHUB_ASTRO_REPO/owner or a token that
  // can't see the private repo also 404s. verifyAccess() throws on those, so a
  // misconfig fails the run loudly instead of silently going dark as
  // target_missing; only a genuinely-absent file reaches the skip below.
  if (!existing) {
    await gh.verifyAccess();
    logger.error(`[backlink-sync] ${ASTRO_FILE} missing on default branch (astro PR not merged) — skipping`);
    return { wrote: false, reason: 'target_missing', changed: true, count: total };
  }

  const content = buildPayload(offices);
  const message = `chore(data): sync ${total} verified entity profiles`;
  const prTitle = `Sync ${total} verified entity profiles (Backlink Manager)`;
  const prBody = buildPrBody(offices, total, currentTotal);
  const { owner, repo } = gh.env();

  // Reuse an OPEN sync PR if one is already awaiting review — human review can
  // take longer than the weekly interval, and the default branch keeps the old
  // set until merge, so a fresh branch each run would stack duplicate PRs.
  // Live path (dry-run/gate-off already returned) — do NOT swallow a failed
  // lookup: if it errors we can't tell whether a sync PR is already open, and
  // falling through would stack a duplicate. Let it throw so the next run reconciles.
  const openPrs = await gh.ghFetchPaginated(`/repos/${owner}/${repo}/pulls?state=open`);
  const openSyncPr = (openPrs || []).find(
    (p) => p && p.head && typeof p.head.ref === 'string' && p.head.ref.startsWith(SYNC_BRANCH_PREFIX),
  );

  if (openSyncPr) {
    const branch = openSyncPr.head.ref;
    const onBranch = await gh.getFile(ASTRO_FILE, branch); // null on 404, throws on auth/network
    // Skip if the open PR's branch already carries this exact set — `changed` was
    // computed against the default branch, so a still-open PR keeps landing here;
    // re-writing would churn a no-op commit (generatedAt differs), rerun CI, and
    // invalidate an already-approved diff.
    if (canonicalOffices(parseCurrentOffices(onBranch)) === canonicalOffices(offices)) {
      logger.info(`[backlink-sync] open PR ${openSyncPr.html_url} already current (${total} profiles) — no update`);
      return { wrote: false, reason: 'pr_already_current', count: total, pr: openSyncPr.html_url, branch };
    }
    await gh.putFile({ path: ASTRO_FILE, content, message, branch, sha: onBranch ? onBranch.sha : existing.sha });
    // Refresh title/body too — the diff now reflects a different set, so stale
    // count/URL review instructions would otherwise mislead the reviewer.
    await gh.updatePr(openSyncPr.number, { title: prTitle, body: prBody });
    logger.info(`[backlink-sync] updated open PR ${openSyncPr.html_url} (${total} profiles)`);
    return { wrote: true, updatedExisting: true, count: total, pr: openSyncPr.html_url, branch };
  }

  const stamp = new Date().toISOString().replace(/[^0-9]/g, '').slice(0, 14);
  const branch = `${SYNC_BRANCH_PREFIX}${stamp}`;
  await gh.createBranch(branch);
  await gh.putFile({ path: ASTRO_FILE, content, message, branch, sha: existing.sha });
  const pr = await gh.createPr({ head: branch, title: prTitle, body: prBody });
  logger.info(`[backlink-sync] opened PR ${pr.html_url} (${total} profiles)`);
  return { wrote: true, count: total, pr: pr.html_url, branch };
}

module.exports = { syncProfilesToAstro, getVerifiedProfilesByOffice };
