const db = require('../models/db');

function dateOnly(value) {
  if (!value) return null;
  if (value instanceof Date) return value.toISOString().slice(0, 10);
  return String(value).split('T')[0].slice(0, 10);
}

function isAutoGeneratedPayPerApplicationInvoice(invoice = {}) {
  const title = String(invoice.title || '').toLowerCase();
  const notes = String(invoice.notes || '').toLowerCase();
  return notes.includes('auto-generated from accepted estimate')
    && notes.includes('customer selected pay per application')
    && (title.includes('first application') || notes.includes('first application'));
}

async function findFirstApplicationInvoiceForEstimateService(svc, conn = db) {
  const sourceEstimateId = svc?.source_estimate_id;
  const customerId = svc?.customer_id;
  const scheduledDate = dateOnly(svc?.scheduled_date);
  if (!sourceEstimateId || !customerId || !scheduledDate) return null;

  const rows = await conn('invoices as i')
    .join('scheduled_services as first_visit', 'i.scheduled_service_id', 'first_visit.id')
    .where('i.customer_id', customerId)
    .where('first_visit.source_estimate_id', sourceEstimateId)
    .where('first_visit.scheduled_date', scheduledDate)
    .whereNot('i.status', 'void')
    .orderBy('i.created_at', 'desc')
    .select('i.*');

  return (rows || []).find(isAutoGeneratedPayPerApplicationInvoice) || null;
}

module.exports = {
  dateOnly,
  findFirstApplicationInvoiceForEstimateService,
  isAutoGeneratedPayPerApplicationInvoice,
};
