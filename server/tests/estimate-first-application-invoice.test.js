const {
  findFirstApplicationInvoiceForEstimateService,
  isAutoGeneratedPayPerApplicationInvoice,
} = require('../services/estimate-first-application-invoice');

function makeKnex(rows = []) {
  const calls = [];
  const chain = {
    join: jest.fn((...args) => {
      calls.push(['join', ...args]);
      return chain;
    }),
    where: jest.fn((...args) => {
      calls.push(['where', ...args]);
      return chain;
    }),
    whereNot: jest.fn((...args) => {
      calls.push(['whereNot', ...args]);
      return chain;
    }),
    orderBy: jest.fn((...args) => {
      calls.push(['orderBy', ...args]);
      return chain;
    }),
    select: jest.fn(async (...args) => {
      calls.push(['select', ...args]);
      return rows;
    }),
  };
  const knex = jest.fn((table) => {
    calls.push(['table', table]);
    return chain;
  });
  knex.calls = calls;
  return knex;
}

describe('estimate first-application invoice lookup', () => {
  test('classifies only auto-generated pay-per-application first-application invoices', () => {
    expect(isAutoGeneratedPayPerApplicationInvoice({
      title: 'WaveGuard Membership Setup + First Application',
      notes: 'Auto-generated from accepted estimate #est-1. Customer selected pay per application - $99 setup fee plus first application.',
    })).toBe(true);

    expect(isAutoGeneratedPayPerApplicationInvoice({
      title: 'WaveGuard Bronze - Annual Prepay (12 months)',
      notes: 'Auto-generated from accepted estimate #est-1. Customer selected "Pay the year upfront" - $99 setup fee waived.',
    })).toBe(false);

    expect(isAutoGeneratedPayPerApplicationInvoice({
      title: 'First Service Application',
      notes: 'Created manually by office.',
    })).toBe(false);

    expect(isAutoGeneratedPayPerApplicationInvoice({
      title: 'WaveGuard Membership Setup',
      notes: 'Auto-generated from accepted estimate #est-1. Customer selected pay per application - $99 setup fee only.',
    })).toBe(false);
  });

  test('finds sibling first-application invoice for same estimate date', async () => {
    const annualInvoice = {
      id: 'annual-invoice',
      title: 'WaveGuard Bronze - Annual Prepay (12 months)',
      notes: 'Auto-generated from accepted estimate #est-1. Customer selected "Pay the year upfront" - $99 setup fee waived.',
    };
    const firstApplicationInvoice = {
      id: 'first-app-invoice',
      title: 'WaveGuard Membership Setup + First Application',
      notes: 'Auto-generated from accepted estimate #est-1. Customer selected pay per application - $99 setup fee plus first application.',
    };
    const knex = makeKnex([annualInvoice, firstApplicationInvoice]);

    const result = await findFirstApplicationInvoiceForEstimateService({
      customer_id: 'customer-1',
      source_estimate_id: 'est-1',
      scheduled_date: '2026-06-08',
    }, knex);

    expect(result).toBe(firstApplicationInvoice);
    expect(knex.calls).toContainEqual(['table', 'invoices as i']);
    expect(knex.calls).toContainEqual(['join', 'scheduled_services as first_visit', 'i.scheduled_service_id', 'first_visit.id']);
    expect(knex.calls).toContainEqual(['where', 'i.customer_id', 'customer-1']);
    expect(knex.calls).toContainEqual(['where', 'first_visit.source_estimate_id', 'est-1']);
    expect(knex.calls).toContainEqual(['where', 'first_visit.scheduled_date', '2026-06-08']);
    expect(knex.calls).toContainEqual(['whereNot', 'i.status', 'void']);
  });

  test('does not query when the service is not linked to an estimate date', async () => {
    const knex = makeKnex();

    await expect(findFirstApplicationInvoiceForEstimateService({
      customer_id: 'customer-1',
      scheduled_date: '2026-06-08',
    }, knex)).resolves.toBeNull();

    expect(knex).not.toHaveBeenCalled();
  });
});
